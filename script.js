const container = document.querySelector(".produtos");
const seccao = document.querySelector(".seccao_especifica");
const containerGeral = document.querySelector("#moda-infantil .container");

container.innerHTML = ""; // Limpa produtos anteriores

modaInfantil.forEach(prod => {
  const divProduto = document.createElement("div");
  divProduto.classList.add("produto");

  divProduto.innerHTML = `
    <img class="imagem" src="${prod.imagem}" alt="${prod.nome}" style="width: 150px;">
    <h3 class="nome_produto">${prod.nome}</h3>
    <button class="preco">R$${prod.preco}</button>
    <p class="descricao">${prod.descricao}</p>
  `;

  // Evento de clique no produto
  divProduto.addEventListener("click", () => {
    containerGeral.style.display = "none"; // Oculta produtos

    // Coleta imagens extras
    const imagensExtras = [prod.imagem];
    for (let i = 1; i <= 8; i++) {
      const key = "imagem" + i;
      if (prod[key]) imagensExtras.push(prod[key]);
    }

    // Exibe a seção específica
    seccao.innerHTML = `
      <div class="detalhes-produto">
        <button class="fechar-detalhes"><i class="bi bi-x-circle"></i></button>
        <h3>${prod.nome}</h3>
        <div class="detalhes">
          <img class="principal" src="${prod.imagem}" alt="${prod.nome}">
          <div class="informacoes">
            <p><strong>Preço à vista:</strong> R$${prod.preco}</p>
            <p><strong>Preço no cartão:</strong> R$${prod.preco_cartao}</p>
            <p><strong>descrição: </strong>${prod.descricao}</p>
            <button class="adicionar-carrinho">Adicionar ao Carrinho</button>
          </div>
        </div>
        <div class="galeria">
          ${imagensExtras.map(src => `<img src="${src}" >`).join("")}
        </div>
      </div>
    `;
    seccao.style.display = "block";

    // Evento para trocar imagem principal ao clicar nas miniaturas
    const imgPrincipal = seccao.querySelector(".principal");
    seccao.querySelectorAll(".galeria img").forEach(mini => {
      mini.addEventListener("click", () => {
        imgPrincipal.src = mini.src;
      });
    });

    // Evento para fechar
    seccao.querySelector(".fechar-detalhes").addEventListener("click", () => {
      seccao.style.display = "none";
      containerGeral.style.display = "block";
    });

    // Evento "Adicionar ao carrinho"
    seccao.querySelector(".adicionar-carrinho").addEventListener("click", () => {
      alert(`Produto "${prod.nome}" adicionado ao carrinho!`);
    });
  });

  container.appendChild(divProduto);
});